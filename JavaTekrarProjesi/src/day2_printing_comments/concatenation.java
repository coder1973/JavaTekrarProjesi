package day2_printing_comments;

public class concatenation {

	public static void main(String[] args) {
		System.out.println("Hello, my number is 10");
		System.out.println("Hello, my number is " + "10");
		System.out.println("Hello, my number is " + 10);
		
		int num = 10;
		System.out.println("Hello, my number is " + num);
		
		

	}

}
